#!/usr/bin/env python3
"""Aggregates every results/*.{json,csv,txt} file produced by the other
benchmark scripts into one results/report.md, with one table per metric and
tools placed side by side. Read benchmarks/README.md for what each metric
means and the fairness rules (cold vs. batch regimes, etc.) baked into how
the numbers were collected.

Run after (some subset of) run_startup.sh, run_cold.sh, run_batch.sh,
run_micro.sh have produced output in benchmarks/results/. Missing inputs are
skipped with a note rather than treated as a hard error, so partial runs
still produce a partial report.
"""
import csv
import json
import re
from pathlib import Path

SCRIPT_DIR = Path(__file__).resolve().parent
BENCH_DIR = SCRIPT_DIR.parent
REPO_DIR = BENCH_DIR.parent
RESULTS_DIR = BENCH_DIR / "results"
TOOLS = ["gopdfrab", "verapdf", "pdfbox"]
TOOL_LABEL = {
    "gopdfrab": "gopdfrab",
    "verapdf": "veraPDF",
    "pdfbox": "PDFBox Preflight",
}
SIZES = ["small", "median", "large"]


def fmt_ms(seconds):
    return f"{seconds * 1000:.2f} ms"


def fmt_bytes(n):
    if n is None:
        return "n/a"
    for unit in ("B", "KB", "MB", "GB"):
        if abs(n) < 1024:
            return f"{n:.1f} {unit}"
        n /= 1024
    return f"{n:.1f} TB"


def load_hyperfine_json(path):
    if not path.exists():
        return None
    data = json.loads(path.read_text())
    results = data.get("results") or []
    return results[0] if results else None


def parse_gnu_time(text):
    """Extracts wall-clock seconds, max RSS (KB), and CPU% from a GNU
    `time -v` report (which may have other stdout/stderr noise around it)."""
    out = {}
    m = re.search(r"Elapsed \(wall clock\) time.*?:\s*([\d:.]+)", text)
    if m:
        out["wall_seconds"] = parse_time_field(m.group(1))
    m = re.search(r"Maximum resident set size \(kbytes\):\s*(\d+)", text)
    if m:
        out["max_rss_kb"] = int(m.group(1))
    m = re.search(r"Percent of CPU this job got:\s*(\d+)%", text)
    if m:
        out["cpu_percent"] = int(m.group(1))
    m = re.search(r"Exit status:\s*(\d+)", text)
    if m:
        out["exit_status"] = int(m.group(1))
    return out


def parse_time_field(s):
    """GNU time formats elapsed as [h:]mm:ss[.ss] or just ss.ss."""
    parts = s.split(":")
    parts = [float(p) for p in parts]
    seconds = 0.0
    for p in parts:
        seconds = seconds * 60 + p
    return seconds


def load_verapdf_json(path):
    """veraPDF has no #summary line; --format json gives per-file
    size/duration/compliance directly, which is also what's used for the
    throughput-vs-size metric (7) and for deriving pass/fail counts here."""
    if not path.exists():
        return None
    data = json.loads(path.read_text())
    jobs = data.get("report", {}).get("jobs", [])
    out = []
    for j in jobs:
        item = j.get("itemDetails", {})
        proc = j.get("processingTime", {})
        results = j.get("validationResult") or [{}]
        out.append({
            "path": item.get("name", ""),
            "size_bytes": item.get("size", 0),
            "nanos": proc.get("difference", 0) * 1_000_000,  # ms -> ns
            "compliant": bool(results[0].get("compliant", False)),
        })
    return out


def load_csv_rows(path):
    if not path.exists():
        return None
    with open(path) as f:
        return list(csv.DictReader(f))


def find_summary_line(text):
    for line in text.splitlines():
        if line.startswith("#summary "):
            try:
                return json.loads(line[len("#summary "):])
            except json.JSONDecodeError:
                return None
    return None


def section(title):
    return f"\n## {title}\n\n"


def main():
    lines = []
    lines.append("# gopdfrab benchmark report\n")
    lines.append(
        "Generated by `benchmarks/scripts/report.py`. See `benchmarks/README.md` "
        "for methodology, caveats, and what each metric measures. Two regimes "
        "are reported separately and should always be read together: **cold** "
        "(includes process/VM startup — realistic for one-shot CLI use) and "
        "**batch** (amortized across the whole corpus in one process — isolates "
        "the verification engine).\n"
    )

    # --- Metric 1: startup overhead -------------------------------------
    lines.append(section("1. Startup overhead (version/usage/module-load only)"))
    lines.append("| Tool | Mean | Stddev | Min | Max |\n|---|---|---|---|---|\n")
    any_startup = False
    for tool in TOOLS:
        r = load_hyperfine_json(RESULTS_DIR / f"startup_{tool}.json")
        if r is None:
            continue
        any_startup = True
        lines.append(
            f"| {TOOL_LABEL[tool]} | {fmt_ms(r['mean'])} | {fmt_ms(r['stddev'])} "
            f"| {fmt_ms(r['min'])} | {fmt_ms(r['max'])} |\n"
        )
    if not any_startup:
        lines.append("*(no data — run scripts/run_startup.sh)*\n")

    # --- Metric 2: cold single-file latency ------------------------------
    lines.append(section("2. Cold single-file latency (includes process/VM startup)"))
    for size in SIZES:
        lines.append(f"\n**{size}**\n\n")
        lines.append("| Tool | Mean | Stddev | Min | Max |\n|---|---|---|---|---|\n")
        any_cold = False
        for tool in TOOLS:
            r = load_hyperfine_json(RESULTS_DIR / f"cold_{tool}_{size}.json")
            if r is None:
                continue
            any_cold = True
            lines.append(
                f"| {TOOL_LABEL[tool]} | {fmt_ms(r['mean'])} | {fmt_ms(r['stddev'])} "
                f"| {fmt_ms(r['min'])} | {fmt_ms(r['max'])} |\n"
            )
        if not any_cold:
            lines.append("*(no data — run scripts/run_cold.sh)*\n")

    # --- Metrics 3-5: batch throughput, memory, CPU ----------------------
    lines.append(section("3-5. Batch run: throughput, peak memory, CPU utilization"))
    lines.append(
        "One process per tool, walking both vendored corpora together "
        "(tests/Isartor/PDFA-1b + "
        "tests/veraPDF/PDF_A-1b, 773 PDFs).\n\n"
    )
    lines.append(
        "| Tool | Files | Wall-clock | Files/s | MB/s | Max RSS | CPU% |\n"
        "|---|---|---|---|---|---|---|\n"
    )
    any_batch = False
    for tool in TOOLS:
        time_path = RESULTS_DIR / f"batch_{tool}_time.txt"
        if not time_path.exists():
            continue
        any_batch = True
        text = time_path.read_text()
        gt = parse_gnu_time(text)
        summary = find_summary_line(text)

        if summary:
            files = summary.get("files")
            total_bytes = summary.get("total_bytes")
            files_per_sec = summary.get("files_per_sec")
            mb_per_sec = summary.get("mb_per_sec")
            max_rss_kb = summary.get("max_rss_kb")
        else:
            # veraPDF doesn't self-report a #summary; derive everything from
            # its own --format json output (see load_verapdf_json) plus GNU
            # time's wall-clock.
            jobs = load_verapdf_json(RESULTS_DIR / f"batch_{tool}.json") or []
            files = len(jobs)
            total_bytes = sum(j["size_bytes"] for j in jobs)
            wall = gt.get("wall_seconds")
            files_per_sec = files / wall if wall else None
            mb_per_sec = (total_bytes / (1024 * 1024)) / wall if wall and total_bytes else None
            max_rss_kb = gt.get("max_rss_kb")

        wall_str = f"{gt['wall_seconds']:.2f} s" if gt.get("wall_seconds") is not None else "n/a"
        fps_str = f"{files_per_sec:.1f}" if files_per_sec else "n/a"
        mbps_str = f"{mb_per_sec:.2f}" if mb_per_sec else "n/a"
        rss_str = fmt_bytes(max_rss_kb * 1024) if max_rss_kb else "n/a"
        cpu_str = f"{gt['cpu_percent']}%" if gt.get("cpu_percent") is not None else "n/a"

        lines.append(
            f"| {TOOL_LABEL[tool]} | {files if files else 'n/a'} | {wall_str} "
            f"| {fps_str} | {mbps_str} | {rss_str} | {cpu_str} |\n"
        )
    if not any_batch:
        lines.append("*(no data — run scripts/run_batch.sh)*\n")
    lines.append(
        "\n*Verdict counts (pass/fail/valid/invalid) are recorded as a sanity "
        "check only — the tools use different conformance interpretations and "
        "are not being scored against each other for correctness here.*\n"
    )

    # --- Metric 6: in-process microbenchmark -----------------------------
    lines.append(section("6. In-process Go microbenchmark (gopdfrab only, noise-free)"))
    bench_path = RESULTS_DIR / "micro_benchstat.txt"
    raw_path = RESULTS_DIR / "micro_raw.txt"
    chosen = bench_path if bench_path.exists() else raw_path
    if chosen.exists():
        lines.append("```\n" + chosen.read_text().strip() + "\n```\n")
    else:
        lines.append("*(no data — run scripts/run_micro.sh)*\n")

    # --- Metric 7: throughput vs. file size ------------------------------
    lines.append(section("7. Throughput vs. file size"))
    lines.append(
        "Per-file timings from the same batch run (metrics 3-5), bucketed by "
        "file size, to separate fixed per-file overhead from cost that scales "
        "with content size. Buckets: <10 KB, 10-100 KB, 100 KB-1 MB, >1 MB.\n\n"
    )
    buckets = [("<10 KB", 0, 10 * 1024), ("10-100 KB", 10 * 1024, 100 * 1024),
               ("100 KB-1 MB", 100 * 1024, 1024 * 1024), (">1 MB", 1024 * 1024, float("inf"))]

    def per_file_rows(tool):
        if tool == "verapdf":
            jobs = load_verapdf_json(RESULTS_DIR / "batch_verapdf.json")
            if jobs is None:
                return None
            return [(j["size_bytes"], j["nanos"]) for j in jobs]
        rows = load_csv_rows(RESULTS_DIR / f"batch_{tool}.csv")
        if rows is None:
            return None
        return [(int(r["size_bytes"]), int(r["nanos"])) for r in rows]

    lines.append("| Size bucket | " + " | ".join(TOOL_LABEL[t] for t in TOOLS) + " |\n")
    lines.append("|---|" + "---|" * len(TOOLS) + "\n")
    any_size_data = False
    per_tool_rows = {t: per_file_rows(t) for t in TOOLS}
    for label, lo, hi in buckets:
        cells = []
        for tool in TOOLS:
            rows = per_tool_rows.get(tool)
            if rows is None:
                cells.append("n/a")
                continue
            in_bucket = [(s, n) for s, n in rows if lo <= s < hi]
            if not in_bucket:
                cells.append("-")
                continue
            any_size_data = True
            total_bytes = sum(s for s, _ in in_bucket)
            total_nanos = sum(n for _, n in in_bucket)
            mbps = (total_bytes / (1024 * 1024)) / (total_nanos / 1e9) if total_nanos else 0
            file_word = "file" if len(in_bucket) == 1 else "files"
            cells.append(f"{mbps:.2f} MB/s ({len(in_bucket)} {file_word})")
        lines.append(f"| {label} | " + " | ".join(cells) + " |\n")
    if not any_size_data:
        lines.append("\n*(no data — run scripts/run_batch.sh)*\n")

    # --- Metric 8: footprint ---------------------------------------------
    lines.append(section("8. Library / deployment footprint"))
    footprint_path = RESULTS_DIR / "footprint.csv"
    if footprint_path.exists():
        lines.append("| Tool | Artifact | Size | Notes |\n|---|---|---|---|\n")
        with open(footprint_path) as f:
            for row in csv.DictReader(f):
                lines.append(
                    f"| {row['tool']} | {row['artifact']} "
                    f"| {fmt_bytes(int(row['bytes']))} | {row['description']} |\n"
                )
    else:
        lines.append("*(no data — run scripts/run_micro.sh)*\n")

    report_path = RESULTS_DIR / "report.md"
    report_path.write_text("".join(lines))
    print(f"wrote {report_path}")


if __name__ == "__main__":
    main()
